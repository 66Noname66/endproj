package org.example.minionsapi.dto;

public class MinionRequest {

    private String name;
    private int age;
    private String city;
    private String skill;
    private String type;

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getCity() {
        return city;
    }

    public String getSkill() {
        return skill;
    }

    public String getType() {
        return type;
    }
}
